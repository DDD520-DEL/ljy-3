export function searchCelestialObjects(query: string): CelestialObject[] {
  if (!query.trim()) return [];
  const lowerQuery = query.toLowerCase().trim();
  return CELESTIAL_CATALOG.filter((obj) => {
    if (obj.name.toLowerCase().includes(lowerQuery)) return true;
    if (obj.commonNames?.some((n) => n.toLowerCase().includes(lowerQuery))) return true;
    if (obj.constellation?.toLowerCase().includes(lowerQuery)) return true;
    if (obj.id.toLowerCase().includes(lowerQuery)) return true;
    return false;
  });
}

export function getCelestialObjectById(id: string): CelestialObject | undefined {
  return CELESTIAL_CATALOG.find((obj) => obj.id === id);
}
